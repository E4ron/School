        <h2>Образование и формы обучения</h2>
        <p>
        С учетом потребностей и возможностей личности образовательные программы осваиваются в следующих формах: в образовательном учреждении - в форме очной, в форме семейного образования, самообразования, экстерната.

Допускается сочетание различных форм получения образования в Школе.

Для всех форм получения образования в пределах конкретной основной общеобразовательной про-граммы действует федеральный государственный образовательный стандарт.

 С учетом интересов родителей (законных представителей) по согласованию с Учредителем в Школе могут открываться специальные (коррекционные) классы для обучающихся с ограниченными возможностями здоровья.

Школа обеспечивает организацию обучения учащихся, нуждающихся в длительном лечении, а также детей-инвалидов на дому на основании медицинского заключения и заявления родителей (законных пред-ставителей). При организации обучения на дому индивидуальный учебный план и расписание занятий со-гласовываются с родителями (законными представителями) учащегося.

При реализации образовательных программ независимо от форм получения образования могут приме-няться электронное обучение, дистанционные образовательные технологии в порядке, установленном феде-ральным органом исполнительной власти, осуществляющим функции по выработке государственной поли-тики и нормативно-правовому регулированию в сфере образования.
